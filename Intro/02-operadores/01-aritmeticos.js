let a = 5;

let b = 3;

let suma = a + b; // adicion 
console.log(suma); // 8

let resta = a - b; // sustraccion
console.log(resta); // 2

let multiplicacion = a * b; // multiplicacion
console.log(multiplicacion); // 15

let division = a / b; // division
console.log(division); // 1.6666666666666667

let modulo = a % b; // modulo
console.log(modulo); // 2

let incremento = a++; // incremento
console.log(incremento); // 5

let decremento = a--; // decremento
console.log(decremento); // 6


